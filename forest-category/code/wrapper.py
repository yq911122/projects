class wrapper(object):
	"""
	wrapper model for feature selection.
	"""
	def __init__(self, X, Y, clf, alg):
		super(wrapper, self).__init__()
		self.X = X
		self.Y = Y
		self.alg = alg(self.X, self.Y, clf)

	def select_features(self):
		return self.alg.train()


